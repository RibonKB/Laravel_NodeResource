All resources are distributed through the organization nodes
created in <strong>Nodify</strong>. This is a tree hierarchical
structure, which allows a top-down distribution.
<br><br>
This section was made to distribute resources in a
non-hierarchical way, giving more flexibility when sharing
resources between nodes.
<br><br>
<ol>
    <li>Create a Distribution Node.</li>
    <li>Assigns one to many organizational nodes.</li>
    <li>
        All resources associated with this distribution node will be shared by
        the related organizational hierarchy.
    </li>
</ol>
